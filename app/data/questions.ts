export function makeRoundQuestions(difficulty: Difficulty, count = 5): Question[] {
  const pool = difficulty === "all" ? [...QUESTIONS] : QUESTIONS.filter((question) => question.difficulty === difficulty);
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  if (shuffled.length >= count) return shuffled.slice(0, count);
  return Array.from({ length: count }, (_, index) => shuffled[index % shuffled.length]);
}
